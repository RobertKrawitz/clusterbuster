class TestGoldenObjectCounts:
    """Verify object counts match between Python and bash."""

    @pytest.mark.parametrize("workload,dep_type", _CASES, ids=[_case_id(c) for c in _CASES])
    def test_namespace_count(self, workload, dep_type, capsys):
        golden = _load_golden(workload, dep_type)
        raw = _python_dryrun(workload, dep_type, capsys)
        py_objs = _extract_python_objects(raw)
        g_ns = [o for o in golden if o["kind"] == "Namespace"]
        p_ns = [o for o in py_objs if o["kind"] == "Namespace"]
        assert len(p_ns) == len(g_ns), (
            f"[{workload}/{dep_type}] Namespace count: "
            f"golden={len(g_ns)}, python={len(p_ns)}"
        )

    @pytest.mark.parametrize("workload,dep_type", _CASES, ids=[_case_id(c) for c in _CASES])
    def test_namespace_names(self, workload, dep_type, capsys):
        golden = _load_golden(workload, dep_type)
        raw = _python_dryrun(workload, dep_type, capsys)
        py_objs = _extract_python_objects(raw)
        g_names = sorted(o["name"] for o in golden if o["kind"] == "Namespace")
        p_names = sorted(o["name"] for o in py_objs if o["kind"] == "Namespace")
        assert p_names == g_names

    @pytest.mark.parametrize("workload,dep_type", _CASES, ids=[_case_id(c) for c in _CASES])
    def test_worker_object_count(self, workload, dep_type, capsys):
        """Same number of worker role objects (Pod/Deployment/RS/VM)."""
        golden = _load_golden(workload, dep_type)
        raw = _python_dryrun(workload, dep_type, capsys)
        py_objs = _extract_python_objects(raw)
        g_workers = _worker_objects(golden)
        p_workers = _worker_objects(py_objs)
        assert len(p_workers) == len(g_workers), (
            f"[{workload}/{dep_type}] Worker object count: "
            f"golden={len(g_workers)} ({[o['kind'] for o in g_workers]}), "
            f"python={len(p_workers)} ({[o['kind'] for o in p_workers]})"
        )

    @pytest.mark.parametrize("workload,dep_type", _CASES, ids=[_case_id(c) for c in _CASES])
    def test_sync_pod_exists(self, workload, dep_type, capsys):
        raw = _python_dryrun(workload, dep_type, capsys)
