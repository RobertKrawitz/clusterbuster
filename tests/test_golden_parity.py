def _normalize_uuid(val: str) -> str:
    val = val.replace(FIXED_UUID, "UUID")
    return _UUID_RE.sub("XUUID", val)


def _extract_object_info(doc: dict[str, Any]) -> dict[str, Any]:
    """Extract structural info from a parsed k8s manifest."""
    meta = doc.get("metadata", {})
    kind = doc.get("kind", "")
    info: dict[str, Any] = {
        "kind": kind,
        "name": _normalize_uuid(str(meta.get("name", ""))),
        "namespace": _normalize_uuid(str(meta.get("namespace", ""))),
    }

    labels = meta.get("labels", {})
    if labels:
        info["labels"] = {
            k: _normalize_uuid(str(v)) for k, v in sorted(labels.items())
        }

    spec = doc.get("spec", {})
    if kind == "Service":
        ports = spec.get("ports", [])
        if ports:
            info["ports"] = [
                {
                    "port": p.get("port"),
                    "protocol": p.get("protocol", "TCP"),
                }
                for p in ports
            ]
        selector = spec.get("selector", {})
        if selector:
            info["selector"] = {
                k: _normalize_uuid(str(v)) for k, v in sorted(selector.items())
            }
        svc_type = spec.get("type", "")
        if svc_type:
            info["service_type"] = svc_type
        if spec.get("clusterIP") in ("None", None) and "clusterIP" in spec:
            info["headless"] = True

    if kind == "Pod":
        restart = spec.get("restartPolicy")
        if restart:
            info["restart_policy"] = restart
        containers = spec.get("containers", [])
        if containers:
            sc = containers[0].get("securityContext", {})
            if "runAsNonRoot" in sc:
                info["run_as_non_root"] = sc["runAsNonRoot"]
            if sc.get("sysctls"):
                info["container_sysctls"] = [
                    s["name"] for s in sc["sysctls"]
                ]
        if spec.get("affinity", {}).get("podAffinity"):
            info["has_pod_affinity"] = True

    if kind in ("Deployment", "ReplicaSet"):
        tmpl = spec.get("template", {}).get("metadata", {})
        tmpl_labels = tmpl.get("labels", {})
        if tmpl_labels:
            info["template_labels"] = {
                k: _normalize_uuid(str(v))
                for k, v in sorted(tmpl_labels.items())
            }
        sel = spec.get("selector", {}).get("matchLabels", {})
        if sel:
            info["match_labels"] = {
                k: _normalize_uuid(str(v)) for k, v in sorted(sel.items())
            }
        tmpl_spec = spec.get("template", {}).get("spec", {})
        restart = tmpl_spec.get("restartPolicy")
        if restart:
            info["restart_policy"] = restart
        containers = tmpl_spec.get("containers", [])
        if containers:
            sc = containers[0].get("securityContext", {})
            if "runAsNonRoot" in sc:
                info["run_as_non_root"] = sc["runAsNonRoot"]
            if sc.get("sysctls"):
                info["container_sysctls"] = [
                    s["name"] for s in sc["sysctls"]
                ]

    if kind == "VirtualMachine":
        tmpl = spec.get("template", {}).get("metadata", {})
        tmpl_labels = tmpl.get("labels", {})
        if tmpl_labels:
            info["template_labels"] = {
                k: _normalize_uuid(str(v))
                for k, v in sorted(tmpl_labels.items())
            }

        vm_spec = spec.get("template", {}).get("spec", {})
        domain = vm_spec.get("domain", {})
        devices = domain.get("devices", {})

        disks = devices.get("disks", [])
        serials = sorted(d["serial"] for d in disks if d.get("serial"))
        if serials:
            info["vm_disk_serials"] = serials

        volumes = vm_spec.get("volumes", [])
        vol_types = sorted(
            next((k for k in v if k != "name"), "unknown")
            for v in volumes
        )
        if vol_types:
            info["vm_volume_types"] = vol_types

        ci_vols = [v for v in volumes if "cloudInitNoCloud" in v]
        if ci_vols:
            import yaml
            userdata = ci_vols[0]["cloudInitNoCloud"].get("userData", "")
            try:
                ci_doc = yaml.safe_load(userdata)
                if isinstance(ci_doc, dict):
                    bootcmds = ci_doc.get("bootcmd", [])
                    structural = []
                    for cmd in bootcmds:
                        if not isinstance(cmd, str):
                            continue
                        verb = cmd.strip().split()[0].strip("'\"")
                        if verb in ("mkdir", "mount") or verb.startswith("mkfs"):
                            structural.append(verb)
                    if structural:
                        info["vm_bootcmd_structural"] = structural
            except Exception:
                pass

    return info


def _python_dryrun(workload: str, dep_type: str, capsys: Any) -> str:
    """Run Python clusterbuster dry-run and return stdout."""
    from clusterbuster.driver import run_from_argv
    args = [
        "-n",
        f"--uuid={FIXED_UUID}",
        f"--workload={workload}",
        f"--deployment-type={dep_type}",
    ]
    rc = run_from_argv(args)
    assert rc == 0, f"dry-run failed for {workload}/{dep_type}"
    return capsys.readouterr().out


def _extract_python_objects(raw: str) -> list[dict[str, Any]]:
    """Parse Python dry-run YAML output into extracted objects."""
    import yaml

    marker = "--- Manifests ---"
    idx = raw.find(marker)
    if idx >= 0:
        raw = raw[idx + len(marker):]

    objects: list[dict[str, Any]] = []
    for doc_text in raw.split("---"):
        doc_text = doc_text.strip()
        if not doc_text:
            continue
        try:
            doc = yaml.safe_load(doc_text)
        except Exception:
            continue
        if isinstance(doc, dict) and "kind" in doc:
            objects.append(_extract_object_info(doc))
    return objects


def _load_golden(workload: str, dep_type: str) -> list[dict[str, Any]]:
    path = GOLDEN_DIR / f"{workload}_{dep_type}.json"
    if not path.exists():
        pytest.skip(f"Golden file not found: {path.name}")
    return json.loads(path.read_text())


def _by_kind(objects: list[dict[str, Any]]) -> dict[str, list[dict[str, Any]]]:
    result: dict[str, list[dict[str, Any]]] = {}
    for obj in objects:
        result.setdefault(obj["kind"], []).append(obj)
    return result


_SYNC_RE = re.compile(r"\bsync\b")


def _is_sync(obj: dict[str, Any]) -> bool:
    """True if this object belongs to the sync infrastructure.

    Uses word-boundary matching so that the ``synctest`` workload
    (which contains "sync" as a substring) is not misidentified.
    """
    name = obj.get("name", "")
    ns = obj.get("namespace", "")
    return bool(_SYNC_RE.search(name) or _SYNC_RE.search(ns))


def _is_drop_cache(obj: dict[str, Any]) -> bool:
    name = obj.get("name", "")
    return name.endswith("-dc")


def _worker_objects(
    objects: list[dict[str, Any]],
) -> list[dict[str, Any]]:
    """Non-sync, non-drop-cache workload objects."""
    return [
        o for o in objects
        if o["kind"] in _WORKER_KINDS
        and not _is_sync(o)
        and not _is_drop_cache(o)
    ]


def _workload_services(
    objects: list[dict[str, Any]],
) -> list[dict[str, Any]]:
    """Services that are NOT sync-related."""
    return [
        o for o in objects
        if o["kind"] == "Service" and not _is_sync(o)
    ]


def _get_labels(obj: dict[str, Any]) -> dict[str, str]:
    """Return labels from the top-level or template metadata."""
    labels = obj.get("labels", {})
    if not labels:
        labels = obj.get("template_labels", {})
    return labels


