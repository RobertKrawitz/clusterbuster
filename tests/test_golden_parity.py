def _golden_cases() -> list[tuple[str, str]]:
    cases = []
    for wl in WORKLOADS:
        for dt in DEPLOYMENT_TYPES:
            path = GOLDEN_DIR / f"{wl}_{dt}.json"
            if path.exists():
                cases.append((wl, dt))
    return cases


_CASES = _golden_cases()


def _case_id(val: tuple[str, str]) -> str:
    return f"{val[0]}_{val[1]}"


