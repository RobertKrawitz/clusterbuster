    @staticmethod
    def _multi_ns_dryrun(
        workload: str, dep_type: str, num_ns: int, capsys: Any,
    ) -> list[dict[str, Any]]:
        from clusterbuster.driver import run_from_argv
        args = [
            "-n",
            f"--uuid={FIXED_UUID}",
            f"--workload={workload}",
            f"--deployment-type={dep_type}",
            f"--namespaces={num_ns}",
        ]
        rc = run_from_argv(args)
        assert rc == 0, f"dry-run failed for {workload}/{dep_type}/ns={num_ns}"
        return _extract_python_objects(capsys.readouterr().out)

